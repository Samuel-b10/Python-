# Aula 02 continuação - Tipos Primitivos e Saída de Dados

# input tipo: "float"

n = float(input('Digite um valor: '))
print(n)
print(type(n))

print("\n")

# input tipo: "str"

n1 = str(input('Digite um valor: '))
print(n1)
print(type(n1))

print("\n")

# input tipo: "bool"

n2 = bool(input('Digite um valor: '))
print(n2)
print(type(n2))

print("\n")

# input tipo: "int"

n3 = int(input('Digite um valor: '))
print(n3)
print(type(n3))

print("\n")

