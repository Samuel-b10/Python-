# Código em python: operadores aritmeticos

# Declarando variaveis
x = 5
y = 2

# Operadores aritmeticos: resto da divisão = "%"
z = x % y
print(z)
# Resultado: 1

# Operadores aritmeticos: potencia = "**"
w = x ** y
print(w)
# Resultado: 25

# Operadores aritmeticos: divisão inteira = "//"
v = x // y
print(v) 
# Resultado: 2